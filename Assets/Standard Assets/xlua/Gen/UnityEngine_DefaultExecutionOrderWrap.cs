        [MonoPInvokeCallbackAttribute(typeof(LuaCSFunction))]
        static int _g_get_order(RealStatePtr L)
        {
		    try {
                ObjectTranslator translator = ObjectTranslatorPool.Instance.Find(L);
			
                UnityEngine.DefaultExecutionOrder gen_to_be_invoked = (UnityEngine.DefaultExecutionOrder)translator.FastGetCSObj(L, 1);
                LuaAPI.xlua_pushinteger(L, gen_to_be_invoked.order);
            } catch(System.Exception gen_e) {
                return LuaAPI.luaL_error(L, "c# exception:" + gen_e);
            }
            return 1;
        }
        
        
        
		
		
		
		
